
public class ExtendedEuclideanEquation {
	private int t1 , t2 ,t;

	public ExtendedEuclideanEquation(int t1, int t2, int t) {
		super();
		this.t1 = t1;
		this.t2 = t2;
		this.t = t;
	}

	public void setT1(int t1) {
		this.t1 = t1;
	}

	public void setT2(int t2) {
		this.t2 = t2;
	}

	public void setT(int t) {
		this.t = t;
	}

	public int getT1() {
		return t1;
	}

	public int getT2() {
		return t2;
	}

	public int getT() {
		return t;
	}

}